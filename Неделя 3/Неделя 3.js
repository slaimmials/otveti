alert("Игра 'Заполни ящики'\nЦель игры заполнить ящики содержимым конфет до значения 400 единиц.\nКаждый раунд в ящики добавляется случайное кол-во конфет от 0 до 100.\nВсего 3 раунда\nВаша задача досыпать конфеты.")
do {
    let box1 = Math.floor(Math.random() * 100 + 1);
    let box2 = Math.floor(Math.random() * 100 + 1);
    let box3 = Math.floor(Math.random() * 100 + 1);
    for(let part = 1; part <= 3; part++) {
        alert(`В ящики добавлена ${part}-я партия конфет\nСодержимое ящиков:\nЯщик 1: ${box1}\nЯщик 2: ${box2}\nЯщик 3: ${box3}`)
        let doAdd = confirm("Будем досыпать?")
        if (doAdd) {
            let boxIndex = Number(confirm("Введите номер ящикa"))
            if (boxIndex==1) {
                box1 = box1 + Number(confirm("Сколько будем досыпать?"))
            } else if (boxIndex==2) {
                box2 = box2 + Number(confirm("Сколько будем досыпать?"))
            } else if (boxIndex==3) {
                box3 = box3 + Number(confirm("Сколько будем досыпать?"))
            }
        }
        box1 = box1 + Math.floor(Math.random() * 100 + 1);
        box2 = box2 + Math.floor(Math.random() * 100 + 1);
        box3 = box3 + Math.floor(Math.random() * 100 + 1);
    }
    alert(`Результат:\nЯщик 1:${box1}\nЯщик 2:${box2}\nЯщик 3:${box3}`)
    if (box1, box2, box3 == 400) {
        alert("Вы победили!")
    } else if (box1 != 400) {
        alert("Cодержимое ящика 1 неверно!")
    } else if (box2 != 400) {
        alert("Cодержимое ящика 2 неверно!")
    } else if (box3 != 400) {
        alert("Cодержимое ящика 3 неверно!")
    }   
} while(confirm("Ещё раз?"))
