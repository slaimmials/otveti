alert("Игра “Чёт или нечет” - правила просты,\nкомпьютер загадывает число от 1 до 100, а мы отгадываем чётное оно или нет.")
do {
    let winScore = Number(prompt("Выберите число очков для победы(1-100)"))
    let score;
    do {
        let randomNumber = Math.floor(Math.random() * 100 + 1)
        let userAnswer = prompt("Чёт или нечет?")
        if (userAnswer == "Чёт") {
            if (randomNumber%2==0) {
                alert("Верно!\n+1 к счёту")
                score++;
            } else {
                alert("Неверно!")
            }
        } else if (userAnswer == "Нечет") {
            if (randomNumber%2!=0) {
                alert("Верно!\n+1 к счёту")
                score++;
            } else {
                alert("Неверно!")
            }
        }
    } while(score!=winScore)
} while(confirm("Ещё разок?"))
